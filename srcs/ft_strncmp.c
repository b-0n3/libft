#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int i;

	i = ft_memcmp(s1, s2, n);
	if (i != 0)
		return ((i > 0) ? 1 : -1);	
	return (0);
}
